public class StringMethods {
    public static void main(String[] args) {
        String one = "String ONE";
        String two = "String two";

        /**
         * equals:
         *      ex. one.equals(two)
         * */

        /**
         * Compare:
         *      return 0 if equlas
         *      returns positive or negtive number if not
         *      ex. one.compareTo(two);     // -37
         * */

        /**
         * Length:
         *  one.length();   // 10
         * */

        /**
         * Replace
         *      ex. one.replace("tr","l");    // Sling ONE
         */

        /**
         * Substring:
         *      ex. one.substring(0);   // String ONE
         *      ex. one.substring(1);   // tring ONE
         *      ex. one.substring(1,3);   // tr
         * */

        /**
         * IndexOf:
         *      ex. one.indexOf("t");   // 1
         *
         * */

        /**
         * Concatenation:
         *      ex. one + 2 + 3;   // String ONE23
         *      ex. one + (2 + 3)  // String ONE5
         *
         * */

    }
}
